# Ask your user for a number between 0 and 1,000,000,000.
# Use a `while` loop to find the number. When the number is found,
# exit the loop and print the number to the console.
num = int(input('Choose a number between 0 and 1 000 000 :'))
result = 0

while num != result:
    result +=1

print(f" It took {result} attempts to find {num} ")
